fizz_word = fn
 	0, 0, _ -> IO.puts("FizzBuzz")
 	0, _, _ -> IO.puts("Fizz")
 	_, 0, _ -> IO.puts("Buzz")
 	_, _, n -> IO.puts(n)
end

fb = fn n ->
	fizz_word.(rem(n, 3), rem(n, 5), n)
end

